let n1 = 10, n2 = 2;
let operator = '/';

switch (operator) {
    case '+':
        console.log(n1+n2);
        break;
    case '-':
        console.log(n1+n2);
        break;
    case '*':
        console.log(n1*n2);
        break;
    case '/':
        console.log(n1/n2);
        break;
    case '%':
        console.log(n1%n2);
        break;
    default:
        console.log("not a valid case");
}
